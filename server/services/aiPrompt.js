export const AI_SYSTEM_PROMPT =
  "Ты анализируешь OCR-текст из изображения и возвращаешь только валидный JSON.";

export const AI_CATEGORIES = [
  "документ",
  "чек",
  "заметка",
  "скриншот",
  "обучение",
  "финансы",
  "медицина",
  "юридическое",
  "личное",
  "другое"
];

export const AI_SECTIONS = [
  "здоровье",
  "учеба",
  "работа",
  "финансы",
  "дом",
  "контакты",
  "рецепты",
  "документы",
  "идеи",
  "личное",
  "другое"
];

export const AI_TEXT_QUALITY_VALUES = [
  "full_text",
  "fragment",
  "low_confidence",
  "no_meaningful_text"
];

export function buildPrompt(text) {
  return `
Ты обрабатываешь текст, полученный OCR из загруженного изображения.

Главная ценность для пользователя:
- дать понятное название;
- кратко объяснить, что это за документ;
- превратить сырой OCR в аккуратный читаемый текст;
- сохранить смысл и не придумать факты, которых нет в OCR.

Твоя задача:

Ты — эксперт по расшифровке и реставрации текстов после некачественного OCR (распознавания рукописного и печатного текста). 
Твоя цель — сделать текст читаемым, исправить ошибки распознавания, но строго сохранить оригинал.

1. Выполни задачу пошагово:

    Шаг 1. Анализ контекста
    Внимательно прочитай весь OCR-текст. Определи его тип (рецепт, конспект, протокол, список, личные заметки и т.д.) и общую тематику. 
    Используй этот контекст как главный ориентир для исправления поврежденных слов.

    Шаг 2. Очистка и реставрация
    1. Исправь очевидные ошибки распознавания: склеенные или разорванные пробелами слова, перепутанные буквы (например, «ш» вместо «и», «0» вместо «О»), лишние дефисы и переносы строк.
    2. Восстанавливай слова по контексту: 
      - Если слово искажено, но смысл очевиден из темы (например, «Евишня» в списке имен -> «Евгения» или «Евдокия»; «мсло» в рецепте -> «масло»), замени его на правильное.
      - Осторожно раскрывай общепринятые сокращения, если это улучшает читаемость.
    3. Соблюдай границы: Восстановление искаженных букв по контексту НЕ считается добавлением новых фактов. 
    Но тебе строго запрещено добавлять от себя новые даты, суммы, имена, абзацы или выводы, которых вообще не было в исходнике.

    Шаг 3. Работа со спорными местами
    - Если слово полностью превратилось в бессмысленный набор символов (например, «щфцв12») и контекст не помогает, замени его на маркер «[неразборчиво]».
    - Если есть два одинаково подходящих по контексту варианта, выбери тот, который ближе по написанию к OCR-тексту, либо оставь исходное слово, если оно читаемо.

    Шаг 4. Форматирование
    Восстанови логическую структуру: абзацы, списки, заголовки, если они угадываются в исходном тексте. Не используй сложную верстку, только чистый читаемый текст.

    Выведи только очищенный текст, без твоих комментариев и пояснений.



2. Сформировать cleanText:
   - это должен быть основной полезный текст для пользователя;
   - он должен читаться лучше, чем сырой OCR;
   - если OCR похож на список, таблицу, чек, инструкцию, объявление или схему с подписями, оформи cleanText так, чтобы его было удобно читать  - как список или каждое предложение с новой строки;
   - разделяй cleanText на абзацы, если в тексте есть несколько смысловых частей;
   - используй списки с дефисами, если OCR-текст похож на перечень пунктов;
   - используй двоеточия и тире, если это помогает восстановить структуру документа;
   - не используй markdown-заголовки, жирный текст, HTML или таблицы;
   - не превращай весь текст в список, если обычные абзацы читаются лучше;
   - если видна только часть текста, не достраивай недостающее, а отметь фрагментарность в textQuality и notes;
   - если OCR бессмысленный или почти пустой, cleanText оставь пустым или очень коротким.

3. Создать короткий человеческий title.

4. Создать summary в 1-2 предложения:
   - объясни, что это за материал;
   - не пересказывай длинно;
   - не делай выводов, которых нет в тексте.

5. Выбрать одну category строго из списка:
   ${AI_CATEGORIES.map((category) => `"${category}"`).join(", ")}

6. Выбрать один section строго из списка:
   ${AI_SECTIONS.map((section) => `"${section}"`).join(", ")}

7. Создать короткий topic:
   - это смысловая тема внутри section;
   - 1-3 слова;
   - не повторяй section;
   - не делай topic слишком общим вроде "текст", "фото", "важное";
   - примеры: "анализы", "биология", "визитки", "лекарства", "история", "договор".

8. Добавить 3-7 полезных tags.

Верни ТОЛЬКО валидный JSON.
Без markdown.
Без комментариев.
Без текста до или после JSON.

Формат JSON:

{
  "title": "короткий понятный заголовок",
  "summary": "краткое описание в 1-2 предложения",
  "category": "документ",
  "section": "документы",
  "topic": "общая запись",
  "tags": ["тег1", "тег2", "тег3"],
  "cleanText": "очищенный и удобно оформленный текст для пользователя",
  "textQuality": "full_text",
  "notes": ""
}

Правила:
- category должен быть строго одним из значений:
  ${AI_CATEGORIES.map((category) => `"${category}"`).join(", ")}
- section должен быть строго одним из значений:
  ${AI_SECTIONS.map((section) => `"${section}"`).join(", ")}
- topic должен быть короткой смысловой темой внутри section. Если тема неясна, используй пустую строку.
- textQuality должен быть строго одним из значений:
  ${AI_TEXT_QUALITY_VALUES.map((value) => `"${value}"`).join(", ")}
- Если OCR-текст слишком короткий или бессмысленный, установи textQuality = "no_meaningful_text".
- Если это явно только часть большего текста, установи textQuality = "fragment".
- Если текст читается плохо, много неясных мест или распознавание ошиблось во многих местах, установи textQuality = "low_confidence".
- Если текст выглядит цельным и понятным, установи textQuality = "full_text".
- Если OCR-текст на русском, все значения в JSON пиши на русском.
- Если OCR-текст на другом языке, сохрани язык исходного текста для cleanText, а title, summary, category, section, topic, tags и notes можно писать на русском.
- notes оставь пустой строкой, если текст нормальный и дополнительных пояснений не нужно.
- В notes коротко напиши, если текст фрагментарный, плохо читается, похож на набор подписей или OCR не дает полной картины.

OCR-текст:
${text}
`;
}
