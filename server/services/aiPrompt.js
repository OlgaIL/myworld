export const AI_SYSTEM_PROMPT = "Ты анализируешь OCR-текст из изображения и возвращаешь только валидный JSON.";

export const AI_CATEGORIES = [
  "документ",
  "чек",
  "заметка",
  "скриншот",
  "обучение",
  "финансы",
  "медицина",
  "юридическое",
  "личное",
  "другое"
];

export const AI_TEXT_QUALITY_VALUES = [
  "full_text",
  "fragment",
  "low_confidence",
  "no_meaningful_text"
];

export function buildPrompt(text) {
  return `
Ты обрабатываешь текст, полученный OCR из загруженного изображения.

Твоя задача:
1. Аккуратно очистить и исправить OCR-текст:
   - исправь очевидные OCR-ошибки и опечатки
   - сохрани исходный смысл
   - не придумывай факты, которых нет в тексте
   - если текст неполный или похож на фрагмент, явно отметь это

2. Создать короткий понятный заголовок.

3. Создать краткое описание в 1-2 предложения.

4. Выбрать одну категорию из списка:
   ${AI_CATEGORIES.map((category) => `"${category}"`).join(", ")}

5. Добавить 3-7 полезных тегов.

Верни ТОЛЬКО валидный JSON.
Без markdown.
Без комментариев.
Без текста до или после JSON.

Формат JSON:

{
  "title": "короткий понятный заголовок",
  "summary": "краткое описание в 1-2 предложения",
  "category": "документ",
  "tags": ["тег1", "тег2", "тег3"],
  "cleanText": "исправленный полный OCR-текст",
  "textQuality": "full_text",
  "notes": ""
}

Правила:
- category должен быть строго одним из значений:
  ${AI_CATEGORIES.map((category) => `"${category}"`).join(", ")}
- textQuality должен быть строго одним из значений:
  ${AI_TEXT_QUALITY_VALUES.map((value) => `"${value}"`).join(", ")}
- Если OCR-текст слишком короткий или бессмысленный, установи textQuality = "no_meaningful_text".
- Если это явно только часть большего текста, установи textQuality = "fragment".
- Если текст читается плохо, много неясных мест или OCR явно ошибся во многих местах, установи textQuality = "low_confidence".
- Если текст выглядит цельным и понятным, установи textQuality = "full_text".
- Не добавляй факты, имена, даты, суммы или выводы, которых нет в OCR-тексте.
- Ничего не выдумывай.
- cleanText должен быть максимально близок к исходному тексту: исправляй только очевидные OCR-ошибки.
- Если OCR-текст на русском, все значения в JSON пиши на русском.
- Если OCR-текст на другом языке, сохрани язык исходного текста для cleanText, а title, summary, category, tags и notes можно писать на русском.
- notes оставь пустой строкой, если текст нормальный и дополнительных пояснений не нужно.

OCR-текст:
${text}
`;
}
