export const AI_SYSTEM_PROMPT =
  "Ты анализируешь OCR-текст из изображения и возвращаешь только валидный JSON.";

export const AI_CATEGORIES = [
  "документ",
  "чек",
  "заметка",
  "скриншот",
  "обучение",
  "финансы",
  "медицина",
  "юридическое",
  "личное",
  "другое"
];

export const AI_TEXT_QUALITY_VALUES = [
  "full_text",
  "fragment",
  "low_confidence",
  "no_meaningful_text"
];

export function buildPrompt(text) {
  return `
Ты обрабатываешь текст, полученный OCR из загруженного изображения.

Главная ценность для пользователя:
- дать понятное название;
- кратко объяснить, что это за документ;
- превратить сырой OCR в аккуратный читаемый текст;
- сохранить смысл и не придумать факты, которых нет в OCR.

Твоя задача:
1. Аккуратно очистить OCR-текст:
   - исправить очевидные OCR-ошибки, опечатки, лишние переносы и сломанные пробелы;
   - восстановить читаемую структуру текста, если она понятна из OCR;
   - сохранить исходный смысл;
   - не добавлять имена, даты, суммы, выводы или факты, которых нет в OCR;
   - сначала определи наиболее вероятный тип материала и его тему по всему OCR-тексту (например: рецепт, конспект, заметки с собрания, список покупок, инструкция, объявление, документ и т.п.);
   - используй это понимание темы при исправлении OCR-ошибок и восстановлении повреждённых слов;
   - если отдельное слово выглядит как ошибка OCR или ошибка распознавания рукописного текста, попробуй восстановить наиболее вероятный вариант по контексту всего документа;
   - допускается осторожное восстановление сокращений и повреждённых слов, если смысл документа делает исправление очевидным;
   - если документ похож на рецепт, используй кулинарный контекст для восстановления ингредиентов и терминов;
   - если документ похож на конспект или записи с собрания, используй учебный или организационный контекст для восстановления терминов;
   - если существует несколько одинаково вероятных вариантов, не угадывай и оставь вариант, максимально близкий к OCR-тексту;
   - не сохраняй заведомо бессмысленные слова, если их можно уверенно восстановить по контексту;
   - восстановление повреждённых слов по контексту не считается добавлением новых фактов, если исправление логически следует из содержания документа.




2. Сформировать cleanText:
   - это должен быть основной полезный текст для пользователя;
   - он должен читаться лучше, чем сырой OCR;
   - если OCR похож на список, таблицу, чек, инструкцию, объявление или схему с подписями, оформи cleanText так, чтобы его было удобно читать  - как список или каждое предложение с новой строки;
   - разделяй cleanText на абзацы, если в тексте есть несколько смысловых частей;
   - используй списки с дефисами, если OCR-текст похож на перечень пунктов;
   - используй двоеточия и тире, если это помогает восстановить структуру документа;
   - не используй markdown-заголовки, жирный текст, HTML или таблицы;
   - не превращай весь текст в список, если обычные абзацы читаются лучше;
   - если видна только часть текста, не достраивай недостающее, а отметь фрагментарность в textQuality и notes;
   - если OCR бессмысленный или почти пустой, cleanText оставь пустым или очень коротким.

3. Создать короткий человеческий title.

4. Создать summary в 1-2 предложения:
   - объясни, что это за материал;
   - не пересказывай длинно;
   - не делай выводов, которых нет в тексте.

5. Выбрать одну category строго из списка:
   ${AI_CATEGORIES.map((category) => `"${category}"`).join(", ")}

6. Добавить 3-7 полезных tags.

Верни ТОЛЬКО валидный JSON.
Без markdown.
Без комментариев.
Без текста до или после JSON.

Формат JSON:

{
  "title": "короткий понятный заголовок",
  "summary": "краткое описание в 1-2 предложения",
  "category": "документ",
  "tags": ["тег1", "тег2", "тег3"],
  "cleanText": "очищенный и удобно оформленный текст для пользователя",
  "textQuality": "full_text",
  "notes": ""
}

Правила:
- category должен быть строго одним из значений:
  ${AI_CATEGORIES.map((category) => `"${category}"`).join(", ")}
- textQuality должен быть строго одним из значений:
  ${AI_TEXT_QUALITY_VALUES.map((value) => `"${value}"`).join(", ")}
- Если OCR-текст слишком короткий или бессмысленный, установи textQuality = "no_meaningful_text".
- Если это явно только часть большего текста, установи textQuality = "fragment".
- Если текст читается плохо, много неясных мест или OCR ошибся во многих местах, установи textQuality = "low_confidence".
- Если текст выглядит цельным и понятным, установи textQuality = "full_text".
- Если OCR-текст на русском, все значения в JSON пиши на русском.
- Если OCR-текст на другом языке, сохрани язык исходного текста для cleanText, а title, summary, category, tags и notes можно писать на русском.
- notes оставь пустой строкой, если текст нормальный и дополнительных пояснений не нужно.
- В notes коротко напиши, если текст фрагментарный, плохо читается, похож на набор подписей или OCR не дает полной картины.

OCR-текст:
${text}
`;
}
